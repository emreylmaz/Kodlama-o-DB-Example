package Customer;

public class Channel {
     private long id;
     private String ChannelName;

     public long getId() {
          return id;
     }

     public void setId(long id) {
          this.id = id;
     }

     public String getChannelName() {
          return ChannelName;
     }

     public void setChannelName(String channelName) {
          ChannelName = channelName;
     }
}
